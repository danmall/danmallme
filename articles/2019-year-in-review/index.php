                <p><span class="dm-dp-openingLines">I really enjoyed</span> writing <a href="/articles/2018-year-in-review/">last&rsquo;s year in review</a> as it was a good way to assess the year and look forward to the next one. Naturally, here&rsquo;s one for 2019!</p>

                <div class="dm-c-carbonWrap">
