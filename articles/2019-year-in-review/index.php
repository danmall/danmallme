                <p>I&rsquo;ve got a side project or two that I'd like to do around my sneaker collection. Maybe 2019 will be the year. Probably not though. </p>

                <h2>Travel</h2>

                <p>As one who didn&rsquo;t travel much as a kid, I feel very fortunate every year I get to visit other places in the world. In 2018, I took both work and non-work trips to Cambridge, MA; Chicago, IL; Greensboro, NC; Salt Lake City, UT;  Las Vegas, NV; Faroe Islands; New York, NY; Seattle, WA; Toronto, Ontario; Marin County, CA; Cayman Islands; Washington DC; Strathmere, NJ; Mayfield, OH; Brooklyn, NY; Paris, France; Zurich, Switzerland; Orlando, FL; and Houston, TX. </p>

                <p>I&rsquo;m looking forward to visiting more amazing places in 2019.  So far, I&rsquo;ve got trips lined up to Chicago, IL; Austin, TX; Seattle, WA; Boston, MA; and Toronto, Ontario, and likely adding Scotland, Los Angeles, New Zealand, Japan, and China to the list.</p>

                <h2>Conferences, workshops, and training</h2>

                <p>In 2018, I spoke at 11 conferences, both virtually and in-person: <a href="https://www.uxpin.com/design-systems-virtual-summit-2018">UXPin Design Systems Virtual Summit 2018</a>; LoopConf; <a href="https://aycl.uie.com/virtual_seminars/which_new_skills_should_designers_pursue">UIE All You Can Learn Library</a>; Generate Conference NYC; Smashing Conference in <a href="https://smashingconf.com/toronto-2018/">Toronto</a> and <a href="https://smashingconf.com/ny-2018/">New York City</a>; An Event Apart in <a href="https://aneventapart.com/event/washington-dc-2018"> Washington DC</a> and <a href="https://aneventapart.com/event/orlando-2018">Orlando</a>; <a href="https://frontendconf.ch/">Frontend Conference</a>; <a href="https://conference.awwwards.com/new-york/">Awwwards Digital Thinkers Conference</a>; and <a href="https://www.clarityconf.com/2018">Clarity Conference</a>.</p>

                <p>In 2019, I&rsquo;ll be speaking at <a href="https://bureauofdigital.com/event/owner-summit-2019">Owner Summit</a>; An Event Apart in <a href="https://aneventapart.com/event/seattle-2019">Seattle</a> and <a href="https://aneventapart.com/event/boston-2019">Boston</a>; and <a href="https://smashingconf.com/toronto-2019/">Smashing Conference Toronto</a>. I&rsquo;ll be adding to that list as the year progresses, so watch this space.</p>

                <p>I&rsquo;m excited to add more training to my repertoire in 2019. <a href="http://bradfrost.com/">Brad Frost</a> and I are excited to help designers an developers collaborate better, starting with a <a href="https://designdevjan2019.splashthat.com/">workshop in Philly</a>! We'll be doing a few more of these public workshops throughout the year; more details on that soon. If you&rsquo;re interested in us doing a private workshop for your organization, <a href="/contact/">drop me a line</a> and let&rsquo;s talk.</p>

                <p>I&rsquo;m also putting some plans together for some specialized training. I haven&rsquo;t seen a lot of resources to help mid-level designers at startups and agencies get over the hump to being art directors and design directors, so I&rsquo;m developing a curriculum specifically for this. It&rsquo;ll likely be about a week-long, intensive workshop. It won&rsquo;t be cheap, but we&rsquo;ll work hard, eat well, and build a strong support network. More on that soon.</p>

                <p>Video will be a format I lean into much more for 2019. I'm so inspired by watching my friends <a href="https://www.instagram.com/curlswithcoral/">Coral Rosado</a> and <a href="https://m.youtube.com/joshluciano?uid=ub6zWFUf0P9Kp8AI6k797A">Josh Luciano</a> grow their skills and audience on their Instagram and YouTube channels, and I already got a decent taste with the <a href="https://www.invisionapp.com/design-system-manager/expert-advice">Design Systems: Mastering Design at Scale</a> series <a href="https://bigmedium.com/">Josh Clark</a>,  <a href="http://bradfrost.com/">Brad Frost</a>, and I produced with <a href="https://www.invisionapp.com/">InVision</a>, as well as <a href="http://skl.sh/superfriendly">my Skillshare class on Design Systems</a>. I&rsquo;ve officially caught the video bug.</p>

                <h2>Concerts</h2>

                <p>I&rsquo;ve been a musician since I was 3, but I haven&rsquo;t historically gone to many concerts. In 2018, Em and I changed that by going to see Lauryn Hill, Jennifer Lopez, Kendrick Lamar, Kenny Chesney, Beyonc&eacute; &amp; Jay-Z. We were also going to see Justin Timberlake, but <a href="https://consequenceofsound.net/2018/12/justin-timberlake-tour-cancelation-coachella/">he bruised his vocal chords</a>. Feel better soon, JT.</p>

                <h2>Books</h2>

                <p>I did a decent amount of reading in 2018. I read or started to read <cite><a href="https://amzn.to/2BVdMgt">Getting to Yes</a></cite> by Roger Fisher; <cite><a href="https://amzn.to/2VnlWai">Principles: Life and Work</a></cite> by Ray Dalio; <cite><a href="https://amzn.to/2Ru8X7x">Batman: Nightwalker</a></cite> by Marie Lu; <cite><a href="https://amzn.to/2SyPIXC">The Art of Screen Time</a></cite> by Anya Kamenetz; <cite><a href="https://amzn.to/2SyPIXC">I Will Teach You to Be Rich</a></cite> by Ramit Sethi; <cite><a href="https://amzn.to/2SseY1M">It Doesn&rsquo;t Have to be Crazy at Work</a></cite> by Jason Fried and David Heinemeier Hansson; <cite>Batman Vol 1â€“7</cite> by Tom King; <cite><a href="https://amzn.to/2SwMJ29">Artemis</a></cite> by Andy Weir; <cite><a href="https://amzn.to/2SCV98h">The Snowball System</a> by Mo Bunnell; <a href="https://amzn.to/2CM4qoP">The Spaceship Next Door</a></cite> by Gene Doucett; and <cite><a href="https://amzn.to/2Sy3bPs">To Sell is Human</a></cite> by Daniel H. Pink. That feels pretty inadequate compared to Em&rsquo;s goal of 100 books every year, but I&rsquo;ll settle for about one per month.</p>

