                <p>I&rsquo;ll start by assessing how I did against the things I forecasted from last year.</p>

                <h2 id="eleven">Eleven <a class="dm-dp-anchorLink" href="#eleven">#</a></h2>

                <p role="text">Em and I celebrated our 11th anniversary in 2019! We were going to throw a big bash called the <span aria-hidden="true">Ma11 Ba11</span><span class="dm-u-move--offscreen"> Mall Ball</span>&mdash;get it?&mdash;but then we realized that it would really stress out these two introverts to plan and throw a giant party and that we&rsquo;d much rather travel together. So we spent a long weekend in Cuba instead!</p>

                <h2 id="kids">Kids <a class="dm-dp-anchorLink" href="#kids">#</a></h2>

                <p>Sidda and Charlie are now 8 years old and 6 years old respectively. They&rsquo;re getting to those ages where there&rsquo;s lots to share about them, but I&rsquo;m also increasingly more inclined to keep their personal details private. Suffice it to say that the older they get, the more I&rsquo;m enjoying being their dad.</p>

                <h2 id="health-wellness-self-care">Health, wellness, and self-care <a class="dm-dp-anchorLink" href="#health-wellness-self-care">#</a></h2>

                <p>I&rsquo;ve been drinking a lot more water this year, thanks to always having my <a href="https://www.hydroflask.com/21-oz-standard-mouth-flex/color,lava,a,92,o,54">HydroFlask</a> or <a href="https://amzn.to/2tjcV8p">Coca-Cola tumblers</a> filled with ice water nearby. I thought this would help me drink less soda, but alas, I&rsquo;m still as addicted to cola as ever. (IT&rsquo;S SO DELICIOUS.)</p>

                <p>I&rsquo;m happy to report that I&rsquo;m still playing basketball regularly, an average of once every two weeks. An ankle sprain about 2 months ago has put me out of commission recently, but I&rsquo;m <a href="https://twitter.com/danmall/status/1211726228979355648">slowly getting back into atheltic activity again</a>.</p>

                <p>One thing I&rsquo;m really happy about is that I&rsquo;m eating at home more now. I really enjoy cooking, but very rarely do I block out the time to do it. So, I end up defaulting to fast food a lot. Over the last 6 months, however, subscriptions to meal services <a href="https://my.tovala.com/referral/JPP5WDZR">Tovala</a> and <a href="https://eat.cookunity.com/landing-referral?referral_code=danmal96/">CookUnity</a> have got me eating quick and delicious meals, and I&rsquo;m also saving a little money every month too.</p>

                <p><a href="/articles/my-experience-with-laser-eye-surgery/">I finally got laser eye surgery</a> this year. <abbr title="Too long; didn&rsquo;t read">TL;DR</abbr>: if you can deal with a first few hours of inconvenience, it&rsquo;s awesome. Almost one year later, I&rsquo;m really loving the little things, like forgetting to bring contact solution on a trip or worrying about a contact lens getting bumped out while playing sports.</p>

                <p>I wanted to get my second tattoo this year, but it didn&rsquo;t happen. But! It&rsquo;s definitely happening in 2020. I have the appointment already booked, and bonus: my brother and I are gonna get the same one together.</p>

                <p>I started therapy again this year after about a year-long break. It&rsquo;s been helpful at times, but I think I need to be clearer with myself (and my therapist) about what I want out of it.</p>

