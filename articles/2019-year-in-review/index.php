                <p>I also gave myself a lot more permission to not finish books this year. I think this comes from a combination of 2 things:</p>

                <ol>
                    <li>In <a href="https://shawnblanc.net/2016/08/i-buy-more-books-than-i-read/">I Buy More Books Than I Read</a>, Shawn Blanc says, &ldquo;All I care about is getting one good idea or story from that book. If I get that, then it&rsquo;s worth it to me&hellip; A life- and business-changing idea for $10 or less? Sold.&rdquo;</li>
                    <li>Audible has <a href="https://audible.custhelp.com/app/answers/detail/a_id/4592/~/can-i-return%2Fexchange-my-book%3F">a very generous return policy</a>: &ldquo;If you&rsquo;re unhappy with your book, you can return or exchange it easily as long as it&rsquo;s within 365 days of the original purchase. Our exchange policy is risk-free. It allows active members to take a chance on a new narrator or story without losing a credit. Our ability to continue to offer this benefit is dependent on our members using it for this purpose only.&rdquo; Because of this generosity, I return audiobooks liberally while being conscious about whether or not I&rsquo;m abusing this policy, but I also purchase audiobooks liberally too. This kind of policy feels like it&rsquo;s coming from a company that respects its customers, and I&rsquo;m happy to reward that kind of company with my business.</li>
                </ol>


