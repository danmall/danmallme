            <p><span class="dm-dp-openingLines">After four days</span> of working together, she finally admitted, &ldquo;No one ever taught me how to scope.&rdquo; I was working with one of my clients&rsquo; senior developers to map out exactly how we were going to get all of the work done that we needed, but a few frustrating hours of going in circles finally revealed the culprit. It&rsquo;s a problem I&rsquo;ve seen from very junior to some of the most senior employees I&rsquo;ve worked with.</p>

            <p>Simply put, I like to define <dfn>scope</dfn> as:</p>
            <ol>
                <li>A description of all the work that needs to get done, and</li>
                <li>The time needed to do said work.</li> 
            </ol>

            <p>That can be as simple as building a website in 5 days or as complex as organizing an entire ad campaign. Regardless, the formula remains intact.</p>

            <p>Good scoping is often a case of simple arithmetic; by the time you were 8 or 9 years old, you&rsquo;ve had the skills to scope well. So why is it so difficult? You&rsquo;re mostly fighting the psychology of it all.</p>

            <p>If I asked you if you could build a website (of any given complexity) in 6 months, I&rsquo;d bet your answer would be, &ldquo;Probably.&rdquo; That&rsquo;s the <a href="https://en.wikipedia.org/wiki/Valence_effect">valence effect</a> at play: your tendency to think that more good will happen to you than bad. Throw a little <a href="https://en.wikipedia.org/wiki/Parkinson%27s_law">Parkinson&rsquo;s law</a> in there (the adage that work expands to fill the time available for its completion) and that explains why you&rsquo;ve taken way longer on every project than you should have&mdash;and felt good about it to boot. The reality is closer to <a href="https://en.wikipedia.org/wiki/Hofstadter%27s_law">Hofstadter&rsquo;s law</a>: it always takes longer than you expect (even when you take into account Hofstadter&rsquo;s law). Rather than relying on gut feelings, we can use math to figure out if you&rsquo;ll meet your deadlines.</p>

            <h2 id="budgeting">Budgeting time <a class="dm-dp-anchorLink" href="#budgeting-time">#</a></h2>

            <p>If you&rsquo;re like most people (and like me), it&rsquo;s difficult to save money. Let&rsquo;s say you&rsquo;re trying to save $6,000 for a vacation next year. That&rsquo;s a daunting task at first glance, but it becomes easier when you break it down. Instead of looking at it as $6k, look at it as $500/month ($6,000 &divide; 12 months). Or maybe it&rsquo;s $125/week ($500 &divide; 4 weeks). Or $25/day ($125 &divide; 5 days). When you look at it that way, for every day you go to work, skip your Starbucks coffee, pack a lunch, throw that $25 in a jar, and watch that vacation happen in no time. By breaking it down into digestible chunks, it&rsquo;s much more manageable!</p>

            <p>If it helps, think of scoping like a budget for your time.</p>

