            <p>You can continue to break down each task further if it&rsquo;s useful to do so, but, even at this level, you can realistically assess the work. Can you write a section of <abbr>HTML</abbr> in 30 minutes? If yes, great! You&rsquo;re on schedule and will make your timeline. If not, what can you adjust or reorder to make it work?</p>

            <p>Herein lies the real beauty of this technique: <strong>you&rsquo;ll know when you&rsquo;re in danger of missing a deadline days, weeks, or even months in advance.</strong> For example, if you haven&rsquo;t completed Section 3 of Page 1&rsquo;s <abbr>HTML</abbr> by Monday at 10:30am, you&rsquo;re going to miss your deadline. Sure, it&rsquo;s possible that you may make up the time later on, but if you&rsquo;ve estimated realistically and remember Hofstadter&rsquo;s law, now&mdash;4 days in advance&mdash;is a good time to ask your client or manager for an extra day, just in case.</p>

            <p>Scoping is about appropriately setting expectations for yourself, your team, and your clients and managers. It&rsquo;s a rare skill that separates the amateurs from the professionals. Which one are you?</p>

