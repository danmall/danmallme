                <p>I&rsquo;m generally a &ldquo;glass half full&rdquo; kind-of-guy, so while 2018 had both ups and downs, it was a pretty good year overall. In no particular order, some observations about my year and what I&rsquo;m looking forward to for 2019. </p>

                <h2>Ten</h2>

                <p><a href="http://emilymall.me/">Em</a> and I celebrated our ten-year wedding anniversary in 2018! We wanted to do a big celebration bash, but alas: we&rsquo;re both pretty terrible at follow-through for this kind of thing, so it never happened. We&rsquo;ve hired some help this time around, so hopefully we&rsquo;ll celebrate our 11th anniversary in 2019 with the Ma11 Ba11 (I know, I know&hellip; my logo designer is showing).</p>

                <h2>Kids</h2>

                <p>Parenting 2 daughters is more fun than it&rsquo;s ever been. I&rsquo;ll admit: as much as I tried to be a present and active dad, I wasn&rsquo;t really sure how to have fun and play with them as babies. That was really difficult for me. Now that they&rsquo;re older, I love so many more of the things we can do together! We've been playing a lot of board games like Clue, Battleship, and Taboo. </p>

                <p>Sidda reads books and comics in her bed as a way to fall asleep every night. (She&rsquo;s currently into the <a href="https://amzn.to/2SAUsMI">Raina Telgemeier</a> series.)  She started ice skating lessons too and is loving that. We're talking about taking a trip together soon, just the two of us.</p>

                <p>Charlie&rsquo;s asked me to give her piano lessons and practices on her own without prompting. She's frustrated that she can&rsquo;t read like big sis, which I think is the exact motivation that&rsquo;s going to get her to read. She draws letters constantly. </p>

                <p>They both learned to swim this summer; Sidda&rsquo;s pretty fearless in the water now, while Char is still a bit hesitant. I bet one more summer of swimming will build that full confidence.</p>

                <p>They&rsquo;re also in the same school again after a year of being in different schools. It's a little weird as Seventh-day Adventists to have them in a Catholic school, but the school is great and it gives us the opportunity to talk about things like confirmation and baptism and whether or not they want to take part, which we leave up to them. It's also interesting to see them figure out how their relationship as sisters compares to their relationship to their friends.</p>

                <p>Also, I think they both have boyfriends now.</p>

                <p><img src="raven.gif" alt="Dread" /></p>

                <p>I&rsquo;m looking forward to more adventures with them in 2019.</p>

                <h2>Taking care of my body</h2>

                <p>I did some better things for my mind and body this past year. </p>

                <p>I love soda and I really don&rsquo;t like drinking water. That&rsquo;s not a great combo, so I changed some of that around in 2018. First, I realized that I much prefer cold water to room temp, so I got a <a href="https://www.hydroflask.com/21-oz-standard-mouth-flex/color,lava,a,92,o,54">HydroFlask bottle</a> that&rsquo;s always filled with ice water. I also realized that some flavor in my water goes a long way, so my bottle is always filled with lemon, lime, oranges, strawberries, mint, cantaloupe, watermelon, or some combination of those. Those factors increased my water intake from virtually none to some. I also think that means that I drink soda less, but that might just be an unsubstantiated feeling. I quit soda in 2013 for about a year but am back on the sauce; perhaps 2019 is the year I quit again, or at least reduce my soda intake some.</p>

                <p>I played basketball just about every Monday night. It&rsquo;s a competitively friendly full court run for 2 hours. Looking forward to continuing that again in 2019.</p>

                <p>I also made more of a concerted effort to lift or do some form of calisthenics a few times a week. I&rsquo;m less regular during busy work weeks, but I was going at least once or twice a week for a good stretch. Rather than do an intense program, <a href="https://twitter.com/octaviusanewman">Octavius Newman</a> turned me on to Jim Wendler&rsquo;s <a href="https://www.lift.net/workout-routines/wendler-5-3-1/">5/3/1 workout</a>, which is more about a slow and methodical way to build strength over a long term. This is much more my speed and pace, as I tend to get discouraged over workout programs if I lose my regularity about them.</p>

                <p>I&rsquo;m getting tired of contacts every day like I&rsquo;ve had for the last 20 years, so I&rsquo;d like to investigate laser eye surgery for 2019.</p>

                <p>I&rsquo;ve also had the idea for a few years to get the same or similar tattoo as my <a href="http://v2.danielmall.com/archives/2006/06/26/grandpa.php">grandfather</a> had from his time in the Filipino navy. After a conversation about tattoos with <a href="https://trentwalton.com/">Trent Walton</a> earlier this year, I think 2019 is the year that I make this happen.</p>

                <h2>Good for the sole</h2>

                <p>I really indulged my sneaker fetish this year. </p>

                <p>I added these to my collection: Jordan 1 Mid University Gold; Jordan 1 Retro High OG Top 3; Air Max 97 JDI; Nike Acronym x Air Presto Mid Racer Pink; Nike Acronym x Air Presto Mid Dynamic Yellow; Nike Acronym x Air Presto Mid Cool Grey; Jordan 1 &ldquo;Homage to Home&rdquo;; Jordan 1 Mid Royal Splatter; Puma Roma; Nike Air Safari OG 2018; Jordan 1 Black/White RE2PECT; Nike SF Air Force 1; Jordan 1 Ret Hi NYC; and Air Jordan 1 Retro High.</p>

                <p>I&rsquo;ve got a side project or two that I'd like to do around my sneaker collection. Maybe 2019 will be the year. Probably not though. </p>

                <h2>Travel</h2>

                <p>As one who didn&rsquo;t travel much as a kid, I feel very fortunate every year I get to visit other places in the world. In 2018, I took both work and non-work trips to Cambridge, MA; Chicago, IL; Greensboro, NC; Salt Lake City, UT;  Las Vegas, NV; Faroe Islands; New York, NY; Seattle, WA; Toronto, Ontario; Marin County, CA; Cayman Islands; Washington DC; Strathmere, NJ; Mayfield, OH; Brooklyn, NY; Paris, France; Zurich, Switzerland; Orlando, FL; and Houston, TX. </p>

                <p>I&rsquo;m looking forward to visiting more amazing places in 2019.  So far, I&rsquo;ve got trips lined up to Chicago, IL; Austin, TX; Seattle, WA; Boston, MA; and Toronto, Ontario, and likely adding Scotland, Los Angeles, New Zealand, Japan, and China to the list.</p>

                <h2>Conferences, workshops, and training</h2>

                <p>In 2018, I spoke at 11 conferences, both virtually and in-person: <a href="https://www.uxpin.com/design-systems-virtual-summit-2018">UXPin Design Systems Virtual Summit 2018</a>; LoopConf; <a href="https://aycl.uie.com/virtual_seminars/which_new_skills_should_designers_pursue">UIE All You Can Learn Library</a>; Generate Conference NYC; Smashing Conference in <a href="https://smashingconf.com/toronto-2018/">Toronto</a> and <a href="https://smashingconf.com/ny-2018/">New York City</a>; An Event Apart in <a href="https://aneventapart.com/event/washington-dc-2018"> Washington DC</a> and <a href="https://aneventapart.com/event/orlando-2018">Orlando</a>; <a href="https://frontendconf.ch/">Frontend Conference</a>; <a href="https://conference.awwwards.com/new-york/">Awwwards Digital Thinkers Conference</a>; and <a href="https://www.clarityconf.com/2018">Clarity Conference</a>.</p>

                <p>In 2019, I&rsquo;ll be speaking at <a href="https://bureauofdigital.com/event/owner-summit-2019">Owner Summit</a>; An Event Apart in <a href="https://aneventapart.com/event/seattle-2019">Seattle</a> and <a href="https://aneventapart.com/event/boston-2019">Boston</a>; and <a href="https://smashingconf.com/toronto-2019/">Smashing Conference Toronto</a>. I&rsquo;ll be adding to that list as the year progresses, so watch this space.</p>

                <p>I&rsquo;m excited to add more training to my repertoire in 2019. <a href="http://bradfrost.com/">Brad Frost</a> and I are excited to help designers an developers collaborate better, starting with a <a href="https://designdevjan2019.splashthat.com/">workshop in Philly</a>! We'll be doing a few more of these public workshops throughout the year; more details on that soon. If you&rsquo;re interested in us doing a private workshop for your organization, <a href="/contact/">drop me a line</a> and let&rsquo;s talk.</p>

                <p>I&rsquo;m also putting some plans together for some specialized training. I haven&rsquo;t seen a lot of resources to help mid-level designers at startups and agencies get over the hump to being art directors and design directors, so I&rsquo;m developing a curriculum specifically for this. It&rsquo;ll likely be about a week-long, intensive workshop. It won&rsquo;t be cheap, but we&rsquo;ll work hard, eat well, and build a strong support network. More on that soon.</p>

                <p>Video will be a format I lean into much more for 2019. I'm so inspired by watching my friends <a href="https://www.instagram.com/curlswithcoral/">Coral Rosado</a> and <a href="https://m.youtube.com/joshluciano?uid=ub6zWFUf0P9Kp8AI6k797A">Josh Luciano</a> grow their skills and audience on their Instagram and YouTube channels, and I already got a decent taste with the <a href="https://www.invisionapp.com/design-system-manager/expert-advice">Design Systems: Mastering Design at Scale</a> series <a href="https://bigmedium.com/">Josh Clark</a>,  <a href="http://bradfrost.com/">Brad Frost</a>, and I produced with <a href="https://www.invisionapp.com/">InVision</a>, as well as <a href="http://skl.sh/superfriendly">my Skillshare class on Design Systems</a>. I&rsquo;ve officially caught the video bug.</p>

                <h2>Concerts</h2>

                <p>I&rsquo;ve been a musician since I was 3, but I haven&rsquo;t historically gone to many concerts. In 2018, Em and I changed that by going to see Lauryn Hill, Jennifer Lopez, Kendrick Lamar, Kenny Chesney, Beyonc&eacute; &amp; Jay-Z. We were also going to see Justin Timberlake, but <a href="https://consequenceofsound.net/2018/12/justin-timberlake-tour-cancelation-coachella/">he bruised his vocal chords</a>. Feel better soon, JT.</p>

                <h2>Books</h2>

                <p>I did a decent amount of reading in 2018. I read or started to read <cite><a href="https://amzn.to/2BVdMgt">Getting to Yes</a></cite> by Roger Fisher; <cite><a href="https://amzn.to/2VnlWai">Principles: Life and Work</a></cite> by Ray Dalio; <cite><a href="https://amzn.to/2Ru8X7x">Batman: Nightwalker</a></cite> by Marie Lu; <cite><a href="https://amzn.to/2SyPIXC">The Art of Screen Time</a></cite> by Anya Kamenetz; <cite><a href="https://amzn.to/2SyPIXC">I Will Teach You to Be Rich</a></cite> by Ramit Sethi; <cite><a href="https://amzn.to/2SseY1M">It Doesn&rsquo;t Have to be Crazy at Work</a></cite> by Jason Fried and David Heinemeier Hansson; <cite>Batman Vol 1â€“7</cite> by Tom King; <cite><a href="https://amzn.to/2SwMJ29">Artemis</a></cite> by Andy Weir; <cite><a href="https://amzn.to/2SCV98h">The Snowball System</a> by Mo Bunnell; <a href="https://amzn.to/2CM4qoP">The Spaceship Next Door</a></cite> by Gene Doucett; and <cite><a href="https://amzn.to/2Sy3bPs">To Sell is Human</a></cite> by Daniel H. Pink. That feels pretty inadequate compared to Em&rsquo;s goal of 100 books every year, but I&rsquo;ll settle for about one per month.</p>

                <h2>Miscellany</h2>
                <ul><li>We got 2 orange tabby kittens: Nala and Simba. They&rsquo;re annoying, but  the kids love them.
                </li><li>We&rsquo;ve been inviting friends over semi-regularly for board games and/or karaoke. They&rsquo;re a ton of fun and laughs; more of this in 2019.
                </li><li>I attended more funerals and viewings than I cared to this year (or ever), including one for my uncle who was the patriarch of our large extended family, as well as for my former college roommate who passed away from Hodgkin's lymphoma. Fuck cancer.
                </li><li>I was <a href="https://twitter.com/danmall/status/915520902607106048">randomly selected for screening at airports</a> 4 times in 2018. 
                </li><li>I wrote <a href="http://danmall.me/articles/">11 blog posts</a> in 2018. That's down up from 5 in 2017, down from 12 in 2016, and on par with 11 in 2015. 
                </li></ul>

                <h2>Clearing my plate for 2019</h2>

                <p>Work-wise, I&rsquo;d like to change things a bit in 2019. I&rsquo;ve spent the last few years learning and practicing how to direct projects. That means largely being in charge of the vision for what SuperFriendly teams would make, and how. That also means that while I was involved in many projects, I was rarely directly responsible for the main output myself. </p>

                <p>For 2019 projects, I&rsquo;m going to try to be responsible for more output myself. That means I&rsquo;ll be working in design tools and code a lot more. I know I typically tend to be in this mode all by myself, so I&rsquo;m going to challenge myself to continue to work with the same amount of people, but more as a collaborator than a director.</p>

                <p>To do this, I know I&rsquo;ll need more familiarity with tools and technologies. I spent some time during the holidays learning the fundamentals of both Rails and Laravel and building a few &ldquo;hello world&rdquo; examples. I&rsquo;m beefing up my React chops in preparation for a few upcoming projects. I have a post drafted about design tooling. I&rsquo;m learning Swift so I can design and build an iPhone app end-to-end myself.</p>

                <p>That&rsquo;s a lot. In order to do all of that, I have to clear my plate a bit of things that I think I&rsquo;m done with, either for now or forever. </p>

                <p>For the last 6 months or so, I&rsquo;ve had the feeling that certain chapters of my life are closing, while others are opening. But I&rsquo;m not sure which ones are which. So, I&rsquo;ve been taking inventory of all the things in my life, both personally and professionally, to see what things have run their course. This has included everything from taking a sabbatical to moving to a different state to shutting down SuperFriendly and taking a full-time job somewhere to being a full-time stay-at-home dad, and everything in-between. I&rsquo;m not quite ready to share any conclusions from this meditation&mdash;not even sure I have any quite yet&mdash;but there are a few things that are a bit clearer than others.</p>

                <ul><li>I&rsquo;ve <a href="/articles/passing-the-superbooked-baton/">passed the baton for SuperBooked</a> to my partner, <a href="https://twitter.com/philipzaengle">Phil Zaengle</a>.  I&rsquo;ll still be involved with this product that I love, but in more less frequent capacity.
                </li><li>Unintentionally, I opened a small <a href="http://v3.danielmall.com/articles/apprenticeships-superfriendly-academy/">design and development apprenticeship</a> in 2012. After almost 7 years, I&rsquo;ve realized that I&rsquo;ve had almost 20 apprentices. That&rsquo;s a lot! I think I&rsquo;ve given all I have to offer here; I no longer have the desire to scratch this particular itch. So, as of 2019, I&rsquo;m closing the apprenticeship. 
                </li></ul>

                <p>I&rsquo;m looking forward to the opportunities this new year can bring that I have some space for. Bring it on, 2019!</p>

